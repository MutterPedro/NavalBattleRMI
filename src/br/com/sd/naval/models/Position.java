package br.com.sd.naval.models;

public class Position {
	private int x;
	private int y;
	private boolean hited;
	
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	public boolean isHited() {
		return hited;
	}
	public void setHited(boolean hited) {
		this.hited = hited;
	}
}
